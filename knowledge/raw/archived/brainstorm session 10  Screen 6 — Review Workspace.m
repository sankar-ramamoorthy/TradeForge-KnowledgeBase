# SECTION 2 — Decision Quality Assessment

This becomes one of the defining TradeForge concepts.

Purpose:

```text id="rv18"
“How good was the overall decision process?”
```

---

# IMPORTANT

Decision quality ≠ outcome.

A losing trade can be:

```text id="rv19"
high-quality decision-making
```

A winning trade can be:

```text id="rv20"
reckless gambling
```

This distinction is critical.

---

# Example

```text id="rv21"
DECISION QUALITY

Preparation:
Strong

Risk Discipline:
Strong

Execution Discipline:
Moderate

Emotional Stability:
Moderate

Context Awareness:
Strong

Overall:
8.1 / 10
```

---

# CRITICAL UX PRINCIPLE

The UI must visually separate:

```text id="rv22"
process quality
```

from:

```text id="rv23"
financial outcome
```

This is foundational.

