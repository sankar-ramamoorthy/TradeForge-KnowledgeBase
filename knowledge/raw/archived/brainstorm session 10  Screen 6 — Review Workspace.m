# Screen 6 — Review Workspace

This is where the system answers the most important question in discretionary trading:

```text id="rv1"
“Did I trade well?”
```

NOT:

```text id="rv2"
“Did I make money?”
```

That distinction is the entire philosophical center of this screen.

---

# Why Screen 6 Matters So Much

Most trading platforms stop at:

* execution
* PnL
* performance metrics

But elite discretionary improvement happens through:

```text id="rv3"
structured reflective review
```

This screen transforms:

```text id="rv4"
experience
```

into:

```text id="rv5"
explicit learning
```

Without this screen, the system risks becoming:

```text id="rv6"
a sophisticated trade tracker
```

With this screen, it becomes:

```text id="rv7"
a disciplined cognitive improvement system
```

---

# Core Purpose Of The Screen

The Review Workspace answers:

```text id="rv8"
“What kind of decision process
did this trade represent?”
```

NOT merely:

```text id="rv9"
“Was this trade profitable?”
```

---

# This Is NOT

## NOT A Performance Report

Avoid:

* giant equity curves
* profit worship
* leaderboard psychology
* dopamine reinforcement

---

# This IS

```text id="rv10"
a structured decision-quality review system
```

Like:

* post-mission analysis
* institutional debrief
* surgical review
* aviation incident review

---

# Psychological Purpose

This screen should encourage:

* honesty
* reflection
* accountability
* pattern awareness
* emotional clarity
* disciplined improvement

NOT:

* shame
* ego defense
* hindsight fantasy
* overfitting

---

# Core UX Principle

The screen should feel like:

```text id="rv11"
a calm reflective debrief
```

NOT:

```text id="rv12"
a report card
```

